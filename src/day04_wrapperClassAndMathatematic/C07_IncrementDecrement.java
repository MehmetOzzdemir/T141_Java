package day04_wrapperClassAndMathatematic;

public class C07_IncrementDecrement {
    public static void main(String[] args) {


        int number = 10;
        number = number + 5;
        System.out.println(number);

        number += 5;
        System.out.println(number);

        int artış = 30;
        number+=artış;
        System.out.println(number);

        number*=2;
        System.out.println(number);
        number++;
        System.out.println(number);
        number-=1;
        System.out.println(number);
        number--;
        System.out.println(number);
    }
}
