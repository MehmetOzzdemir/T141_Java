package day01_javaGiris;

public class C02_VariableOlusturma {
    public static void main(String[] args) {
        int sayi =10;
        System.out.println(sayi);



        sayi=sayi*2;
        System.out.println(sayi);

        sayi=5+sayi;
        System.out.println(sayi);

        //Eşitliğin sol tarafında sadece variable ismi olmalıdı.

        sayi=5*sayi-7;
        System.out.println(sayi);
    }
}
